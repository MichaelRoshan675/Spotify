package spotify;

public class Songs {
	int id;
	String song_name;
	String Artist_name;
	String Production_company;
	
	Songs(){}
	
	Songs(int id,String song_name, String Artist_name, String Production_company){
		this.id = id;
		this.song_name = song_name;
		this.Artist_name = Artist_name ;
		this.Production_company = Production_company;
	}
	
	

}

